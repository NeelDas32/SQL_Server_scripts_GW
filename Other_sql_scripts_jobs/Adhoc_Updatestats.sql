/****** Object:  Step [Updatestats-sit14_cc]    Script Date: 7/29/2024 11:48:35 AM ******/
EXEC @ReturnCode = msdb.dbo.sp_add_jobstep @job_id=@jobId, @step_name=N'Updatestats-sit14_cc', 
		@step_id=1, 
		@cmdexec_success_code=0, 
		@on_success_action=1, 
		@on_success_step_id=0, 
