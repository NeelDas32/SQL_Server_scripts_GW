		@command=N'DECLARE @Database VARCHAR(255)
DECLARE @cmd varchar(1000)

DECLARE DatabaseCursor CURSOR FOR 
SELECT name FROM master.sys.databases  
WHERE state_desc=''ONLINE'' 
and name IN (''sit14_cc'')
ORDER BY 1  

